public class VariableReassignment {
    public static void main(String[] args) {
        // type variableName = value;
        int counter = 0;
        System.out.println(counter);

        counter = 1;
        System.out.println(counter);

        counter = 2;
        System.out.println(counter);

        counter = 3;
        System.out.println(counter);

        counter = 4;
        System.out.println(counter);

        counter = 5;
        System.out.println(counter);

    }
}
