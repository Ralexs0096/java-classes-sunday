public class ArrayClass {
    public static void main(String[] args) {
        int[] nums = { 2, 89, 98 };

        // group - Array
        String[] cars = {
                "Volvo", // 0
                "BMW", // 1
                "Toyota", // 2
        };

        System.out.println(cars[0] + " " + cars[1] + " " + cars[2]);
        System.out.println(nums[0] + " " + nums[1] + " " + nums[2]);
    }
}
